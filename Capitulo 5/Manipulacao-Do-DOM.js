/* Manipulaçao Do DOM: */
/* A Manipulaçao Do DOM )Document Object Model) em JavaScript permite interagir e modificar a estrutura, o conteudo e o estilo dos elementos HTML de uma pagina. */
/* Principais Metodos De Seleçao: 
-> Para acessar elementos do DOM, usa-se: document.getElementById(); Seleciona um elemento pelo ID 
-> document.getElementsByClassName("classe"); Retorna um HTML Collection com os elementos de uma classe
-> document.getElementByTagName("tag"); Retorna um HTML Collection com os elementos da tag
-> document.querySelector("seletor"); Retorna o primeiro elemento que atende ao seletor CSS
-> document.querySelectorAll("selector"); retorna uma NodeList com todos os elementos que correspondem ao Seletor.
*/

/* Manipulaçao De Conteudo: */
/* -> element.innerHTML = "new content"; Define ou obtem HTML dentro do elemento
-> element.textContent = "new content"; Define ou obtem apenas o texto dentro do elemento
-> element.value = "new value"; Define o valor de um input
*/

/* Manipulaçao De Estilos: */
/* -> element.style.property = "value"; Modifica estilos diretamente
Exemplo: element.style.color = "red";
-> element.classList.remove("class"); Remove uma classe CSS
-> element.classList.add("class"); Adiciona uma classe CSS
-> element.classList.toggle("class"); Adiciona se nao tiver, remove se ja tiver
*/

/* Manipulaçao De Atributos: */
/* -> element.setAttribute("attribute", "values"); Define ou modifica um atributo
-> element.getAttribute("attribute"); Obtem o valor de um atributo
-> element.removeAttribute("attribute"); Remove um atributo
*/

/* Manipulaçao Do DOM: */
/* A Manipulaçao Do DOM )Document Object Model) em JavaScript permite interagir e modificar a estrutura, o conteudo e o estilo dos elementos HTML de uma pagina. */
/* Principais Metodos De Seleçao: 
-> Para acessar elementos do DOM, usa-se: document.getElementById(); Seleciona um elemento pelo ID 
-> document.getElementsByClassName("classe"); Retorna um HTML Collection com os elementos de uma classe
-> document.getElementByTagName("tag"); Retorna um HTML Collection com os elementos da tag
-> document.querySelector("seletor"); Retorna o primeiro elemento que atende ao seletor CSS
-> document.querySelectorAll("selector"); retorna uma NodeList com todos os elementos que correspondem ao Seletor.
*/